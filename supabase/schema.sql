-- ==========================================
-- PROFILES TABLE (linked to auth.users)
-- ==========================================
create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  email text unique,
  full_name text,
  avatar_url text,
  role text default 'user' check (role in ('user', 'admin')),
  created_at timestamptz default now()
);

-- Auto-create profile on signup
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, email, full_name, avatar_url)
  values (new.id, new.email, new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'avatar_url');
  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

alter table public.profiles enable row level security;

drop policy if exists "Users can view own profile" on public.profiles;
create policy "Users can view own profile"
on public.profiles for select
to authenticated
using (auth.uid() = id);

drop policy if exists "Users can update own profile" on public.profiles;
create policy "Users can update own profile"
on public.profiles for update
to authenticated
using (auth.uid() = id)
with check (auth.uid() = id);

-- ==========================================
-- STOCK ITEMS TABLE
-- ==========================================
create table if not exists public.stock_items (
  id bigint generated by default as identity primary key,
  name text not null,
  price numeric(14, 2) not null default 0,
  stock integer not null default 0,
  image_url text,
  created_at timestamptz not null default now()
);

create table if not exists public.sales (
  id bigint generated by default as identity primary key,
  sold_at timestamptz not null default now(),
  stock_item_id bigint not null references public.stock_items(id) on update cascade on delete restrict,
  item_name text not null,
  unit_price numeric(14, 2) not null,
  qty integer not null check (qty > 0),
  total_price numeric(14, 2) not null,
  buyer_name text,
  created_at timestamptz not null default now()
);

create table if not exists public.expenses (
  id bigint generated by default as identity primary key,
  bought_at timestamptz not null default now(),
  description text,
  total_cost numeric(14, 2) not null default 0,
  created_at timestamptz not null default now()
);

create or replace function public.create_sale(
  p_stock_item_id bigint,
  p_qty integer,
  p_buyer_name text default null,
  p_sold_at timestamptz default now()
)
returns public.sales
language plpgsql
security definer
set search_path = public
as $$
declare
  item_row public.stock_items;
  inserted_sale public.sales;
  total numeric(14, 2);
begin
  if p_qty <= 0 then
    raise exception 'qty harus lebih dari 0';
  end if;

  select *
  into item_row
  from public.stock_items
  where id = p_stock_item_id
  for update;

  if not found then
    raise exception 'item tidak ditemukan';
  end if;

  if item_row.stock < p_qty then
    raise exception 'stok tidak cukup';
  end if;

  total := item_row.price * p_qty;

  update public.stock_items
  set stock = stock - p_qty
  where id = p_stock_item_id;

  insert into public.sales (
    sold_at,
    stock_item_id,
    item_name,
    unit_price,
    qty,
    total_price,
    buyer_name
  )
  values (
    p_sold_at,
    item_row.id,
    item_row.name,
    item_row.price,
    p_qty,
    total,
    nullif(trim(p_buyer_name), '')
  )
  returning * into inserted_sale;

  return inserted_sale;
end;
$$;

alter table public.stock_items enable row level security;
alter table public.sales enable row level security;
alter table public.expenses enable row level security;

